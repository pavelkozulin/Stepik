if a % 10 == 1:
    if 11 <= a % 100 <= 19:
        print(a, 'программистов')
    else:
        print(a, 'программист')
if 2 <= a % 10 <= 4:
    if 11 <= a % 100 <= 19:
        print(a, 'программистов')
    else:
        print(a, 'программиста')
